# 4. FOREIGN KEY
# 5. DEFAULT

#CHILD TABLE

CREATE TABLE TEMP2(
CUST_ID INT PRIMARY KEY,
SALARY INT DEFAULT 2000,
FOREIGN KEY(CUST_ID)
REFERENCES TEMP1(ID)      
);                                      #COLUM LINK TO TEMP1

