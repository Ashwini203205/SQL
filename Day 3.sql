INSERT INTO TEMP2 (CUST_ID) VALUES (102);    # BYDEFAULT GIVES SALARY 2000.

INSERT INTO TEMP2 VALUES (103,2000);    # NOT INSERT IN TEMP TO CAUSE 103 NOT IN TEMP1

