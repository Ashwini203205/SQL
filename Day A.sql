("YASH");
SELECT * FROM T1;


CREATE TABLE T2 (
    NAME VARCHAR(50)
);
INSERT INTO T2(NAME)
VALUES
("MOHAN"),
("RUPALI");
SELECT * FROM T2;

SELECT T1.NAME, T2.NAME 
FROM T1 JOIN T2
