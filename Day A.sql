#ON OLD TABLE
SELECT S1.NAME, S2.NAME 
FROM STUDENT AS S1
INNER JOIN STUDENT AS S2 
ON S1.ID <>   S2.ID;


#LEFT EXCLESIVE JOIN

SELECT * FROM STUDENT LEFT JOIN COURSE 
ON STUDENT.ID=COURSE.ID
WHERE COURSE.ID IS NULL;


#RIGHT EXCLUSIVE

SELECT * FROM STUDENT RIGHT JOIN COURSE 
ON STUDENT.ID=COURSE.ID   
WHERE STUDENT.ID IS NULL;


#UNION:UNIQUE

SELECT NAME FROM T1
UNION 
SELECT NAME FROM T2;

#UNION ALL: ALL WITH DUPLICATE

SELECT NAME FROM T1
UNION ALL
SELECT NAME FROM T2;
